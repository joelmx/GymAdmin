    public void actualizarAsistenteActual(){
        limpiaAsistenciaNueva();
        this.jlabelAsistenciasNombre.setText(asistenteActual.getNombre());
        if (asistenteActual.getListaAsistencias().size() > 0) {
            int li = asistenteActual.getListaAsistencias().size() - 1;
            Asistencia ultimaAsistencia = asistenteActual.getListaAsistencias().get(li);
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy HH:mm");
            this.jlabelAsistenciaUltimaFecha.setText(sdf.format(ultimaAsistencia.getFechaAsistencia()));
        } else {
            JOptionPane.showMessageDialog(this, "El cliente no tiene asistencias pasadas.");
        }
    }

