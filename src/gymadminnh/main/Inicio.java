    private void jbuttonMostrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonMostrarClienteActionPerformed
        String str = String.valueOf(this.autoComboBoxClientes.getSelectedItem());
        if (str == null || str.isEmpty() || str.equals("") || str.equals("null")) {
            JOptionPane.showMessageDialog(this, "El campo de nombre no puede estar vacio.");
        } else {
            if (str.indexOf(",") < 0) {
                JOptionPane.showMessageDialog(this, "Cliente no esta en la lista.");
            } else {
                String idCliente = str.split(",")[1];
                int id = Integer.parseInt(idCliente);
                clienteActual = new DataUtils().getClienteDB(id);
                if (clienteActual.getIdCliente() < 1 ) {
                    JOptionPane.showMessageDialog(this, "No se encontro el cliente.");
                } else {
                    actualizarClienteActual();
                }

            }
        }
    }//GEN-LAST:event_jbuttonMostrarClienteActionPerformed

