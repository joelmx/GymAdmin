    private void cargarMedidas() {
        ArrayList<MedidasCliente> lmc = new DataUtils().getMedidasCliente(clienteActual.getIdCliente());
        clienteActual.setListaMedidas(lmc);

        if ( lmc.size() > 0 ){
            MedidasCliente um = lmc.get(lmc.size()-1);
            Date fecha = um.getFecha();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            this.jlabelMedidaFecha.setText(sdf.format(fecha).toString());
            this.jlabelMedidaBiceps.setText(um.getBicepDer() + " / " + um.getBicepIzq());
            this.jlabelMedidaPectoral.setText(String.valueOf( um.getPectoral() ) );
            this.jlabelMedidaAbdomen.setText(String.valueOf(um.getAbdomen()));
            this.jlabelMedidaCintura.setText(String.valueOf(um.getCintura()));
            this.jlabelMedidaPeso.setText(String.valueOf(um.getPeso()));
            this.jlabelMedidaGluteos.setText(String.valueOf(um.getGluteos()));
            this.jlabelMedidaCadera.setText(String.valueOf(um.getCadera()));
            this.jlabelMedidaPiernas.setText(um.getPiernaDer() + " / " + um.getPiernaIzq());
        }
    }

    private void cargarMembresias() {
        ArrayList<Membresia> mems = new DataUtils().getMembresias(clienteActual.getIdCliente());
        clienteActual.setListaMembresias(mems);
        
        if ( mems.size() > 0 ) {
            Membresia um = mems.get(mems.size()-1);
            
            Date fechaInicio = um.getFechaInicio();
            Calendar cal = new GregorianCalendar();
            cal.setTime(fechaInicio);
            cal.add(Calendar.MONTH, um.getDuracion());
            Date fechaVence = cal.getTime();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            
            this.jlabelMembresiaVence.setText(sdf.format(fechaVence));
            this.jlabelMembresiaPrecio.setText(String.valueOf(um.getPrecio()));
            this.jxlabelMembresiaDescripcion.setText(um.getDescripcion());
        }
        
    }

    private void actualizarListaNombres(AutoCompletionComboBox accb) {
        actualizarListaClientes();
        accb.removeAllItems();
        for (int i = 0; i <listaClientes.size() ; i++){
            Cliente c = listaClientes.get(i);
            accb.insertItemAt(c.getNombre()+","+c.getIdCliente(), i );
        }
    }
    
