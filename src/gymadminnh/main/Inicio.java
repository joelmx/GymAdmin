    private void jbuttonGuardarAsistenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonGuardarAsistenciaActionPerformed
        Calendar cal = Calendar.getInstance();
        cal.setTime(this.jxdatepickerAsistenciasNuevaFecha.getDate());
        int horas = this.jcbHoras.getSelectedIndex();
        int mins = Integer.parseInt(String.valueOf(this.jcbMins.getSelectedItem()));
        cal.add(Calendar.HOUR_OF_DAY, horas);
        cal.add(Calendar.MINUTE, mins);

        Asistencia a = new Asistencia();
        a.setFechaAsistencia(cal.getTime());
        
        int resultado = 0;
        
        resultado = new DataUtils().agregarAsistenciaDB(a, asistenteActual.getIdCliente());
        
        if (resultado == 1) {
            JOptionPane.showMessageDialog(this, "Datos guardados.");

        } else {
            JOptionPane.showMessageDialog(this, "Hubo un error al guardar los datos. Los datos no han sido guardados.");
        }
        
        limpiaAsistenciaNueva();
    }//GEN-LAST:event_jbuttonGuardarAsistenciaActionPerformed

