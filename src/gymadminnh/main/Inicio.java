    private void jbuttonFinanzasGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonFinanzasGuardarActionPerformed
        Date fecha = this.jxdatepickerFinanzasNuevo.getDate();
        Double precio = 0.0;
        String descr = this.jtfFinanzasDescr.getText();
        boolean ok = true;
        try {
            precio = Double.parseDouble(this.jtfFinanzasCosto.getText());
        } catch (Exception e) {
            ok = false;
        }
        if ( ok ) {
            int resultado = 0;
            if ( this.jcbFinanzasTipo.getSelectedIndex() == 1 ){
                precio = precio * -1;
            }
            resultado = new DataUtils().agregarMovimiento(fecha, precio, descr);
            if (resultado == 1) {
                JOptionPane.showMessageDialog(this, "Datos guardados.");
            } else {
                JOptionPane.showMessageDialog(this, "Hubo un error al guardar los datos. Los datos no han sido guardados.");
            }
        }
        
        this.jxdatepickerFinanzasNuevo.setDate(new Date());
        this.jxdatepickerFinanzasNuevo.setFormats(new SimpleDateFormat("dd/MM/yyyy"));
        
        this.jtfFinanzasCosto.setText("0.0");
        this.jtfFinanzasDescr.setText("");
    }//GEN-LAST:event_jbuttonFinanzasGuardarActionPerformed

