    private void cargarDatos() {
        this.jtfNombre.setText(clienteActual.getNombre());
        this.jtfTel.setText(clienteActual.getTelefono());
        this.jtfEmail.setText(clienteActual.getEmail());
        this.jtfDir.setText(clienteActual.getDireccion());

        Calendar cal = Calendar.getInstance();
        cal.setTime(clienteActual.getNacidoCuando());
        int dia = cal.get(Calendar.DAY_OF_MONTH);
        int mes = cal.get(Calendar.MONTH);
        int ano = cal.get(Calendar.YEAR);

        this.jcomboboxFechaDia.setSelectedItem("" + dia);
        this.jcomboboxFechaMes.setSelectedItem("" + mes);
        this.jcomboboxFechaAno.setSelectedItem("" + ano);
    }
