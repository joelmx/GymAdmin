    private void jbuttonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonGuardarActionPerformed
        String nombre = this.jtfNombre.getText();
        String strNacDia = this.jcomboboxFechaDia.getSelectedItem().toString();
        String strNacMes = this.jcomboboxFechaMes.getSelectedItem().toString();
        String strNacAno = this.jcomboboxFechaAno.getSelectedItem().toString();
        String email = this.jtfEmail.getText();
        String telefono = this.jtfTel.getText();
        String dir = this.jtfDir.getText();
        
        String nac = strNacDia + "/" + strNacMes + "/" + strNacAno;
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        java.util.Date fecha = null;
        try {
            fecha= sdf.parse(nac);
        } catch (ParseException ex) {
            System.out.println(ex);
        }
        java.sql.Date fechaSQL = new java.sql.Date(fecha.getTime());
        
        String[] nombreTemp = nombre.split(" ");
        if ( nombre.isEmpty() || nombreTemp.length < 1){   // checa el nombre no este vacio
            JOptionPane.showMessageDialog(this, "Nombre y Fecha de Nacimiento son obligatorios.");
        } else {
            nombreTemp = null;
            int respuesta = 0;
            if ( esNuevo ) {
                respuesta = new DataUtils().agregarClienteDB(nombre, fechaSQL, telefono, email, dir);
            } else {
                clienteActual.setNombre(nombre);
                clienteActual.setNacidoCuando(fecha);
                clienteActual.setTelefono(telefono);
                clienteActual.setEmail(email);
                clienteActual.setDireccion(dir);
                respuesta = new DataUtils().modificarClienteDB(clienteActual);
            }
            this.jbuttonGuardar.setEnabled(false);
            if (respuesta == 1) {
                listaNombres = new DataUtils().getListaNombres();
                JOptionPane.showMessageDialog(this, "La informacion ha sido guardada.");
            } else {
                this.jbuttonGuardar.setEnabled(false);
                JOptionPane.showMessageDialog(this, "Error al guardar datos. Los datos no se han guardado.");
            }
            this.dispose();
        }
    }//GEN-LAST:event_jbuttonGuardarActionPerformed

