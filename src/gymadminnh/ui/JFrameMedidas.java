    private void jbuttonGuardarMedidasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonGuardarMedidasActionPerformed
        if ( datosValidados()) {
            int resultado = 0;
            if (!esNuevo) {
                resultado = new DataUtils().modificarMedidasDB(medida);
            } else {
                resultado = new DataUtils().agregarMedidasDB(medida, cliente.getIdCliente());
            }
            System.out.println("result: " + resultado);
            if (resultado == 1) {
                JOptionPane.showMessageDialog(this, "Datos guardados.");
                ArrayList<MedidasCliente> almc = new DataUtils().getMedidasCliente(cliente.getIdCliente());
                cliente.setListaMedidas(almc);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Hubo un error al guardar los datos. Los datos no han sido guardados.");
                this.dispose();
            }
        }
    }//GEN-LAST:event_jbuttonGuardarMedidasActionPerformed

