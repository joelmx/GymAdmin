    private boolean esNumero(String text) {
        try {
            double d = Double.parseDouble(text);
        } catch (NumberFormatException nfe) {
            return false;
        }
        return true;
    }
    
    private boolean sonNumero(String [] strs) {
        for (String text : strs) {
            try {
                double d = Double.parseDouble(text);
            } catch (NumberFormatException nfe) {
                return false;
            }
        }
        return true;
    }

    private boolean tienenInfo(String [] strs) {
        for (String str : strs ){
            if ( str.isEmpty() )
                return false;
            if ( str.split(" ").length < 1 )
                return false;
        }
        return true;
    }

    private boolean datosValidados() {
        String strBicepDer = this.jtfBicepDerecho.getText();
        String strBicepIzq = this.jtfBicepIzqierdo.getText();
        String strPectoral = this.jtfPectoral.getText();
        String strAbdomen = this.jtfAbdomen.getText();
        String strCintura = this.jtfCintura.getText();
        String [] strs = {strBicepDer, strBicepIzq, strPectoral, strAbdomen, strCintura};
        
        if ( !tienenInfo(strs) ){
            JOptionPane.showMessageDialog(this, "Biceps, Pectoral, Abdomen y Cintura son datos obligatorios.");
            return false;
        } else if ( !sonNumero(strs) ) {
            JOptionPane.showMessageDialog(this, "Error en el formato de campos obligatorios.");
            return false;
        } else {
            String strPeso = this.jtfPeso.getText();
            String strGluteos = this.jtfGluteos.getText();
            String strCadera = this.jtfCadera.getText();
            String strPiernaDer = this.jtfPiernaDerecha.getText();
            String strPiernaIzq = this.jtfPiernaIzq.getText();

            if (!strPeso.isEmpty()) {
                if (esNumero(strPeso)) {
                    medida.setPeso(Double.parseDouble(strPeso));
                } else {
                    JOptionPane.showMessageDialog(this, "Solo valores numericos.");
                    return false;
                }
            }
            if (!strGluteos.isEmpty()) {
                if (esNumero(strGluteos)) {
                    medida.setGluteos(Double.parseDouble(strGluteos));
                } else {
                    JOptionPane.showMessageDialog(this, "Solo valores numericos.");
                    return false;
                }
            }
            if (!strCadera.isEmpty()) {
                if (esNumero(strCadera)) {
                    medida.setCadera(Double.parseDouble(strCadera));
                } else {
                    JOptionPane.showMessageDialog(this, "Solo valores numericos.");
                    return false;
                }
            }
            if (!strPiernaDer.isEmpty()) {
                if (esNumero(strPiernaDer)) {
                    medida.setPiernaDer(Double.parseDouble(strPiernaDer));
                } else {
                    JOptionPane.showMessageDialog(this, "Solo valores numericos.");
                    return false;
                }
            }
            if (!strPiernaIzq.isEmpty()) {
                if (esNumero(strPiernaIzq)) {
                    medida.setPiernaIzq(Double.parseDouble(strPiernaIzq));
                } else {
                    JOptionPane.showMessageDialog(this, "Solo valores numericos.");
                    return false;
                }
            }
            
            medida.setBicepDer(Double.parseDouble(strBicepDer));
            medida.setBicepIzq(Double.parseDouble(strBicepIzq));
            medida.setPectoral(Double.parseDouble(strPectoral));
            medida.setAbdomen(Double.parseDouble(strAbdomen));
            medida.setCintura(Double.parseDouble(strCintura));
            
            Date fecha = this.jxdatepickerFecha.getDate();
            medida.setFecha(fecha);
            return true;

        }
    }
    
